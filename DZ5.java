public class DZ5 {
    public static void main(String[] args) {

        int a = 0;

        for (int i = 1; i<=6; i++){
            a += i;
            System.out.println(i + ") Num is " + i + ", sum is " + a);
        }
        System.out.println("---------------------- \n" + "Sum of numbers is " + a);
    }
}
